    private void jLabel9MousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel9MousePressed
        if (tField.getText().equals("0") || tField.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Valor no valido, verifique el valor ingresado");
        } else {
            try {
                DolarTemporal(tField.getText());
            } catch (IOException ex) {
                Logger.getLogger(TasaDolares.class.getName()).log(Level.SEVERE, null, ex);
            }
            caja.dolar();
            this.setVisible(false);
            if (!caja.isVisible()) {
                caja.setVisible(true);
                clientes.setVisible(true);
            }

        }
    }//GEN-LAST:event_jLabel9MousePressed

    private void tFieldKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tFieldKeyTyped
        char ch = evt.getKeyChar();
        if (ch < '0' || ch > '9') {
            evt.consume();
        }
    }//GEN-LAST:event_tFieldKeyTyped

    private void tFieldKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tFieldKeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            if (tField.getText().equals("0") || tField.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "Valor no valido, verifique el valor ingresado");
            } else {
                try {
                    DolarTemporal(tField.getText());
                } catch (IOException ex) {
                    Logger.getLogger(TasaDolares.class.getName()).log(Level.SEVERE, null, ex);
                }
                caja.dolar();
                this.setVisible(false);
                if (!caja.isVisible()) {
                    caja.setVisible(true);
                    clientes.setVisible(true);
                }
            }
        }
    }//GEN-LAST:event_tFieldKeyPressed

