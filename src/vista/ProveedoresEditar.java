    private void ActualizarMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ActualizarMousePressed
        p.setIdProveedor(Integer.parseInt(CampoInvisible.getText()));
        p.setNombre(nombreTxt.getText());
        p.setpComercial(propositoTxt.getText());
        p.setRif(rifTxt.getText());
        p.setDireccion(direccionTxt.getText());
        p.setTelefono(telefonoTxt.getText());
        p.setCorreo(correoTxt.getText());

        if (!nombreTxt.getText().isEmpty() && !rifTxt.getText().isEmpty() && !telefonoTxt.getText().isEmpty() && !propositoTxt.getText().isEmpty()) {
            if (ValidarTelefono() == true || telefonoTxt.getText().isEmpty()) {
                cp.editar(p);
                JOptionPane.showMessageDialog(null, "Proveedor actualizado con Exito");
                ProveedoresBuscar pb = new ProveedoresBuscar();
                pb.setSize(700, 450);
                pb.setLocation(0, 0);

                Contenedor.removeAll();
                Contenedor.add(pb, BorderLayout.CENTER);
                Contenedor.revalidate();
                Contenedor.repaint();
            } else {
                JOptionPane.showMessageDialog(null, "Telefono no Valido");
            }
        } else {
            JOptionPane.showMessageDialog(null, "No se pudo actualizar al proveedor ( Nombre, Rif, Telefono, Proposito Comercial (Obligatorios) )");
        }
    }//GEN-LAST:event_ActualizarMousePressed

