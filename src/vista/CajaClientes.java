    private void aceptarTxtMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_aceptarTxtMousePressed
        c.setNombre(nombreTxt.getText());
        c.setApellido(apellidoTxt.getText());
        c.setCedula(Navegador.getText());
        c.setDireccion(direccionTxt.getText());
        c.setTelefono(telefonoTxt.getText());
        c.setCorreo(correoTxt.getText());

        if (!nombreTxt.getText().isEmpty() && !apellidoTxt.getText().isEmpty() && !Navegador.getText().isEmpty()) {
            //if de metodo de duplicacion
            if (!cc.duplicados(Navegador.getText().trim())) {
                //If de validacion de Numeros telefonicos
                if (ValidarTelefono() == true || telefonoTxt.getText().isEmpty()) {
                    cc.insertar(c);
                     try {
                        ClientesTemporal();
                    } catch (IOException ex) {
                        Logger.getLogger(CajaClientes.class.getName()).log(Level.SEVERE, null, ex);
                    }
                     caja.ObtenerCliente(Navegador.getText());
                    JOptionPane.showMessageDialog(null, "Cliente registrado con Exito");
                    this.dispose();
                    try {
                        caja.factura();
                    } catch (IOException ex) {
                        Logger.getLogger(CajaClientes.class.getName()).log(Level.SEVERE, null, ex);
                    }
                    DefaultTableModel model = (DefaultTableModel) caja.Tabla.getModel();
                    model.setRowCount(0);
                    caja.CalcularTotalPagar();

                } else {
                    JOptionPane.showMessageDialog(null, "Telefono no Valido");
                }

            } else {
                if (ValidarTelefono() == true || telefonoTxt.getText().isEmpty()) {
                    try {
                        ClientesTemporal();
                    } catch (IOException ex) {
                        Logger.getLogger(CajaClientes.class.getName()).log(Level.SEVERE, null, ex);
                    }
                    caja.ObtenerCliente(Navegador.getText());
                    cc.editar(c);
                    JOptionPane.showMessageDialog(null, "Cliente actualizado con Exito");
                    this.dispose();
                    try {
                        caja.factura();
                    } catch (IOException ex) {
                        Logger.getLogger(CajaClientes.class.getName()).log(Level.SEVERE, null, ex);
                    }
                    DefaultTableModel model = (DefaultTableModel) caja.Tabla.getModel();
                    model.setRowCount(0);
                    caja.CalcularTotalPagar();
                } else {
                    JOptionPane.showMessageDialog(null, "Telefono no Valido");
                }

            }

        } else {
            JOptionPane.showMessageDialog(null, "No se pudo registrar al cliente ( Nombre, Apellido, Cedula (Obligatorios) )");
        }
    }//GEN-LAST:event_aceptarTxtMousePressed

