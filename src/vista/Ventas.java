    private void listarRegistro() {
        List<Registro> lista = cr.listar();
        Registro = (DefaultTableModel) jTable1.getModel();
        Object[] ob = new Object[7];
        for (int i = 0; i < lista.size(); i++) {
            ob[0] = lista.get(i).getFactura();
            ob[1] = lista.get(i).getCliente();
            ob[2] = lista.get(i).getProducto();
            ob[3] = lista.get(i).getCantidad();
            ob[4] = lista.get(i).getTbs();
            ob[5] = lista.get(i).getTds();
            ob[6] = lista.get(i).getFecha();
            Registro.addRow(ob);
        }
        jTable1.setModel(Registro);
    }

