    private void ActualizarMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ActualizarMousePressed
        c.setIdCliente(Integer.parseInt(CampoInvisible.getText()));
        c.setNombre(nombreTxt.getText());
        c.setApellido(apellidoTxt.getText());
        c.setCedula(cedulaTxt.getText());
        c.setDireccion(direccionTxt.getText());
        c.setTelefono(telefonoTxt.getText());
        c.setCorreo(correoTxt.getText());

        if (!nombreTxt.getText().isEmpty() && !apellidoTxt.getText().isEmpty() && !cedulaTxt.getText().isEmpty()) {
            if (ValidarTelefono() == true || telefonoTxt.getText().isEmpty()) {
                cc.editar(c);
                JOptionPane.showMessageDialog(null, "Cliente actualizado con Exito");
                ClientesBuscar cb = new ClientesBuscar();
                cb.setSize(700, 450);
                cb.setLocation(0, 0);

                Contenedor.removeAll();
                Contenedor.add(cb, BorderLayout.CENTER);
                Contenedor.revalidate();
                Contenedor.repaint();
            } else {
                JOptionPane.showMessageDialog(null, "Telefono no Valido");
            }
        } else {
            JOptionPane.showMessageDialog(null, "No se pudo actualizar al cliente ( Nombre, Apellido, Cedula (Obligatorios) )");
        }
    }//GEN-LAST:event_ActualizarMousePressed

