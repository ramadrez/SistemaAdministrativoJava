    //Metodo del contador
    public int contador() {

        String caracteres = direccionTxt.getText().replaceAll("[' '\n]", "");
        int num = 100 - caracteres.length();

        return num;
    }

    //Metodo para validar numero telefonico
    public boolean ValidarTelefono() {
        boolean res = telefonoTxt.getText().matches("(0416|0426|0424|0414|0412|0261)\\d{7}");
        return res;
    }

